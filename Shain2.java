class Shain2{
	private int shainBangou;
	private String busho;
	private String namae;
	private int kihonkyu;
	
	public Shain2(int shainBangou,String busho,String namae,int kihonkyu){
		this.shainBangou = shainBangou;
		this.busho = busho;
		this.namae = namae;
		this.kihonkyu = kihonkyu;
	}
	public int getShainBangou(){
		return this.shainBangou;
	}
	public String getBusho(){
		return this.busho;
	}
	public String getNamae(){
		return this.namae;
	}
	public int getKihonkyu(){
		return this.kihonkyu;
	}
}